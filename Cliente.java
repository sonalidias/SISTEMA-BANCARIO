public class Cliente extends utilidades{
    
    public static String nome;
    public static String sobrenome;
    public static double saldo;
    public static double limite;

    public void dNome(String nome){
        this.nome = nome;
    }
    public void dSobrenome(String sobrenome){
        this.sobrenome = sobrenome;
    }
    public void dSaldo(double saldo){
        this.saldo = saldo;
    }
    public void dLimite(double limite){
        this.limite = limite;
    }

}
